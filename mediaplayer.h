static const char *VERSION        = "0.0.1";
static const char *DESCRIPTION    = "Mediaplayer plugin for VDR";
static const char *MAINMENUENTRY  = "Mediaplayer";

class cPluginMediaplayer : public cPlugin {
private:
  static string StartPath;
public:
  static volatile int BufferOffset;
  cPluginMediaplayer(void);
  virtual ~cPluginMediaplayer();
  virtual const char *Version(void) { return VERSION; }
  virtual const char *Description(void) { return DESCRIPTION; }
  virtual const char *CommandLineHelp(void);
  virtual bool ProcessArgs(int argc, char *argv[]);
  virtual bool Initialize(void);
  virtual bool Start(void);
  virtual void Stop(void);
  virtual void Housekeeping(void);
  virtual void MainThreadHook(void);
  virtual cString Active(void);
  virtual time_t WakeupTime(void);
  virtual const char *MainMenuEntry(void) { return MAINMENUENTRY; }
  virtual cOsdObject *MainMenuAction(void);
  virtual cMenuSetupPage *SetupMenu(void);
  virtual bool SetupParse(const char *Name, const char *Value);
  virtual bool Service(const char *Id, void *Data = NULL);
  virtual const char **SVDRPHelpPages(void);
  virtual cString SVDRPCommand(const char *Command, const char *Option, int &ReplyCode);
  static string GetStartPath(void) { return StartPath; }
};
